public class Variaveis {
    
    public static void main(String[] args) {

        int x, y, soma;
        x = 100;
        y = 74;
        soma = x + y;

        /* double ou float -- numero decimal */
        double z;
        z = 25.7;

        System.out.println("Você tem \n" + x);
        System.out.println("Você também tem \n" + y);
        System.out.println("A soma deles são \n" + soma);
      /* '\n' serve para quebrar uma linha, e da para usar vários na mesma linha */
      /* 'println' -- pula uma linha para o texto */

      System.out.println("btw você também tem " + z);
    }

}
